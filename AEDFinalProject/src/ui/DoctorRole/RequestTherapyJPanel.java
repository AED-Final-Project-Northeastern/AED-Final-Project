        comboCategory.removeAllItems();
        for(Network n: ecosystem.getNetworkList()){
            if(comboNetwork.getSelectedItem().toString().equalsIgnoreCase(n.getNetworkName())){
//                System.out.println("!!Network: "+n+": "+n.getEnterpriseDirectory().getEnterpriseList());
                for(Enterprise e: n.getEnterpriseDirectory().getEnterpriseList()){
                    if(e.getOrganizationName() != null && comboTherpay.getSelectedItem() != null && e.getOrganizationName().equalsIgnoreCase(comboTherpay.getSelectedItem().toString())){
                        for(Organization o: e.getOrganizationDirectory().getOrganizationList()){
                            comboCategory.addItem(o.getOrganizationName());
                        }
                    }
                }
            }
        }
        
