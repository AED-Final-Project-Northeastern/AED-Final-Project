    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        boolean check;
        check=(!(comboTherpay.getSelectedItem()==null||comboNetwork.getSelectedItem()==null||comboCategory.getSelectedItem()==null));
        
        if(!check){
            JOptionPane.showMessageDialog(null, "Select proper options (or) doctors not available");
            return;
        }        
        PhysicianWorkRequest request = new PhysicianWorkRequest();
        request.setPatientAccount(Patient);
        Organization orga = null;
        request.setSender(accountUser);
        request.setStatus("Requested");

        for(Network n: ecosystem.getNetworkList()){
            if(n.getNetworkName().equalsIgnoreCase(comboNetwork.getSelectedItem().toString())){
                for(Enterprise e: n.getEnterpriseDirectory().getEnterpriseList()){
                    if(e instanceof Treatment&&e.getOrganizationName().equalsIgnoreCase(comboTherpay.getSelectedItem().toString())){
                        for(Organization o:e.getOrganizationDirectory().getOrganizationList()){
                            if(o.getOrganizationName().equalsIgnoreCase(comboCategory.getSelectedItem().toString())){
                                orga=o;
                                o.getWorkQueue().getWorkRequestList().add(request);
                                accountUser.getWorkQueue().getWorkRequestList().add(request);
                                JOptionPane.showMessageDialog(null, "Therapist service requested");
                            }
                        }
                    }
                }
            }
        }
        
        DoctorWorkAreaJPanel sa=new DoctorWorkAreaJPanel(processContainerUser,accountUser,org,Enterprise,ecosystem,Network);
        processContainerUser.add("Customer Adding",sa);
        CardLayout layout=(CardLayout)processContainerUser.getLayout();
        layout.next(processContainerUser);
        
    }//GEN-LAST:event_btnSubmitActionPerformed

